package JUNIT;

public class CalculadoraDemo {

	public static void main(String[] args) {
		
		
		
		
		Calculadora cal = new Calculadora();
		
		
		
		System.out.println("2 + 4 = "+ cal.sumar(2,4));
		System.out.println("2 + 4 = "+ cal.restar(2,4));
		System.out.println("2 + 4 = "+ cal.multiplicar(2,4));
		System.out.println("2 + 4 = "+ cal.dividir(2,4));
		
		
		System.out.println();

	}

}
